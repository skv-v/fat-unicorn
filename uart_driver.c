void uart_rx_string(u8 *buf,u8 max_len){
  u8 i;
  for(i=0; i<max_len; i++){
    while(RI==0);
    RI=0;
    buf[i]=SBUF;
    if(buf[i]=='\r')
      break;
  }
  buf[i]='\0';
}
